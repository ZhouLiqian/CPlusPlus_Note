#include <iostream>
using namespace::std;

/*
 求 1+2+...+n ，要求不能使用乘除法、for、while、if、else、switch、case等关键字及条件判断语句（A?B:C)
 解法：位运算
*/

class Solution {
public:
    int sumNums(int n) {
        bool sum[n][n+1];
        return int(sizeof(sum)) >> 1;
    }
};
